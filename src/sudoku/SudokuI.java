    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean valid = true;
        int [][] maAux = new int[matrix.length][matrix.length];
        for(int row=0; row < matrix.length; row++) {
            for(int col=0; col < matrix[row].length; col++) {
                if(isValidInt(matrix[row][col].getText())) {
                    if(matrix[row][col].getText().equals(""))
                        maAux[row][col] = 0;
                    else
                        maAux[row][col] = Integer.parseInt(matrix[row][col].getText());
                }
                else {
                    matrix[row][col].setBackground(java.awt.Color.RED);
                    jLabel1.setText("Hay uno o más caracteres inválidos en el sudoku");
                    valid = false;
                }
            }
        }
        
        if(valid) {
            Sudoku su = new Sudoku(maAux);
            if(su.getErr() && su.resuelveFinal()) {
                maAux = su.getSu();
                for(int row=0; row < matrix.length; row++) {
                    for(int col=0; col < matrix[row].length; col++) {
                        if(matrix[row][col].getText().equals("")) {
                            matrix[row][col].setText(String.valueOf(maAux[row][col]));
                            matrix[row][col].setBackground(java.awt.Color.GREEN);
                        }    
                    }
                }
            }
            else {
                jLabel1.setText(su.getRes());
            }
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

