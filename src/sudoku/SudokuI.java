    private boolean isValidInt(String casilla) {
        boolean res = false;
        if(casilla.length() == 0 )
            res = true;
        else {
            if(casilla.length() == 1 ) {
                char aux = casilla.charAt(0);
                if(aux >= '1' && aux <= '9')
                    res = true;
            }
        }
        
        return res;
    }
    
    
